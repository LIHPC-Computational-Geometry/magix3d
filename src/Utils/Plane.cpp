Plane::Plane(const XYZ& type, const double offset){
    if(type==XY)
        m_normal = Vector(0,0,1);
    else if(type==XZ)
        m_normal = Vector(0,1,0);
    else
        m_normal = Vector(1,0,0);

    m_point +=offset*m_normal;
}
/*----------------------------------------------------------------------------*/
Plane::Plane (Point& p1, Point& p2, Point& p3)
: m_point(p1)
{
	Vector v1(p1,p2);
	Vector v2(p1,p3);
	m_normal = v1*v2;
}
