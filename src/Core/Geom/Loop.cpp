{}
/*----------------------------------------------------------------------------*/
Loop::~Loop()
{}
/*----------------------------------------------------------------------------*/
void Loop::add(Geom::Curve* curve)
{
    if(std::find(m_curves.begin(),m_curves.end(),curve)==m_curves.end())
        m_curves.push_back(curve);
}
/*----------------------------------------------------------------------------*/
void Loop::get(std::vector<Curve*>& curves) const
{
    curves = m_curves;
}
